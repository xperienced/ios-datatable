#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [self.dataCell.items count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
    }
    
    // Configure the cell...
    NSObject *currentItem = [self.dataCell.items objectAtIndex:indexPath.row];
    
    cell.textLabel.text = self.dataCell.format(currentItem);
    cell.accessoryType = [self.dataCell.items indexOfObject:self.dataCell.value] == indexPath.row ? UITableViewCellAccessoryCheckmark : UITableViewCellAccessoryNone;
    
    return cell;
}

#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    self.dataCell.value = [self.dataCell.items objectAtIndex:indexPath.row];
    [self.dataCell.dataSource setValue:self.dataCell.value forKey:self.dataCell.propertyKey];

    [self.delegate listViewSelectionChanged];
    
    [self.navigationController popViewControllerAnimated:YES];
}

